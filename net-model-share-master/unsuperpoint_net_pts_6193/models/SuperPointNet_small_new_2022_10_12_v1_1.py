class SuperPointNet_small_new_2022_10_12_v1_1(torch.nn.Module):
    """ Pytorch definition of SuperPoint Network. """
    def __init__(self, input_nc=1, output_nc=65, ngf=4, norm_layer=nn.BatchNorm2d, use_dropout=False, n_blocks=3,
                 padding_type='reflect'):
        assert (n_blocks >= 0)
        super(SuperPointNet_small_new_2022_10_12_v1_1, self).__init__()

        # model = [Block(kernel_size=3, in_size=int(1), expand_size=int(4), out_size=int(4),nolinear=hswish(), semodule=SeModule(int(4)), stride=1)]
        # model += [Block(kernel_size=3, in_size=int(4), expand_size=int(4), out_size=int(4),nolinear=hswish(), semodule=SeModule(int(4)), stride=1)]

        model = [
            nn.Conv2d(1, 4, kernel_size=3, padding=1, bias=False),
            norm_layer(4),
            nn.ReLU(True)
        ]

        model += [
            nn.Conv2d(4, 4, kernel_size=3, stride = 1, padding=1, bias=False),
            norm_layer(4),
            nn.ReLU(True)
        ]

        model += [
            nn.Conv2d(4, 8, kernel_size=3, stride = 2, padding=1, bias=False),
            norm_layer(8),
            nn.ReLU(True)
        ]

        # model += [Residual(inp_dim = int(4), out_dim = int(8), k=3, stride=2)]
        model += [Residual(inp_dim = int(8), out_dim = int(16), k=3, stride=2)]
        model += [Residual(inp_dim = int(16), out_dim = int(32), k=3, stride=2)]
        model += [Residual(inp_dim = int(32), out_dim = int(32), k=3, stride=1)]

        self.model = nn.Sequential(*model)

        # Detector Head.
        model_1 = [  # nn.ReflectionPad2d(3),
            nn.Conv2d(32, 64, kernel_size=3, padding=1, bias=False),
            norm_layer(64),
            nn.ReLU(True)]
        model_1 += [nn.Conv2d(64, output_nc, kernel_size=1, padding=0)]
        # model += [hsigmoid()]
        self.model_detector = nn.Sequential(*model_1)

    def forward(self, input):
        x = self.model(input)
        # Detector Head.
        semi = self.model_detector(x)

        output = {'semi': semi, 'desc': "Neon"}

        self.output = output
        return output

    def process_output(self, sp_processer):
        """
        input:
          N: number of points
        return: -- type: tensorFloat
          pts: tensor [batch, N, 2] (no grad)  (x, y)
          pts_offset: tensor [batch, N, 2] (grad) (x, y)
          pts_desc: tensor [batch, N, 256] (grad)
        """
        from utils.utils import flattenDetection
        # from models.model_utils import pred_soft_argmax, sample_desc_from_points
        output = self.output
        semi = output['semi']
        desc = output['desc']
        # flatten
        heatmap = flattenDetection(semi) # [batch_size, 1, H, W] decoder，将深度图转为heatmap
        # nms
        heatmap_nms_batch = sp_processer.heatmap_to_nms(heatmap, tensor=True)
        # extract offsets
        outs = sp_processer.pred_soft_argmax(heatmap_nms_batch, heatmap)
        residual = outs['pred']
        # extract points
        outs = sp_processer.batch_extract_features(desc, heatmap_nms_batch, residual)

        # output.update({'heatmap': heatmap, 'heatmap_nms': heatmap_nms, 'descriptors': descriptors})
        output.update(outs)
        self.output = output
        return output


